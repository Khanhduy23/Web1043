// let là từ khóa dùng để khai báo một biến với phạm vi khối (block scope)
// a là tên biến
let a = 10;
// Set a value of 10
console.log(a);
// alert được sử dụng đề hiển thị một hộp thoại với một thông báo và một nút OK
// alert(a);

// Nhập dữ liệu
// var là từ khóa dùng để khai báo biến (lưu ý rằng let và const là những từ khóa hiện đại hơn thường được khuyến thích sử dụng do có các phạm vi khác nhau và các tính năng bảo mật tốt hơn)
// prompt là hàm dùng để mở một hộp thoại yêu cầu người dùng nhập dữ liệu
var x = prompt("Input n: ");
//Xuất dữ liệu ra màn hình
console.log(x);
document.write("You entered: " + x);
